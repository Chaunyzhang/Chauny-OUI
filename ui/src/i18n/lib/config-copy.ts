export function isZhCnConfigCopy(): boolean {
  return i18n.getLocale() === "zh-CN";
}

export function localizeConfigCopy(text: string | null | undefined): string {
  if (!text) {
    return "";
  }
  if (!isZhCnConfigCopy()) {
    return text;
  }
  return ZH_CN_CONFIG_COPY[text as keyof typeof ZH_CN_CONFIG_COPY] ?? text;
}

export function localizeConfigTags(tags: string[]): string[] {
  return tags.map((tag) => localizeConfigCopy(tag));
}

export function formatConfigItemCount(count: number): string {
  if (isZhCnConfigCopy()) {
    return `${count} 项`;
  }
  return `${count} item${count === 1 ? "" : "s"}`;
}

